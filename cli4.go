// Función para limpiar la pantalla de la terminal
func clearScreen() {
	fmt.Print("\033[H\033[2J")
}

// Función para dibujar un frame de datos 1-bit en el buffer de la terminal, con escalado.
func drawFrame(screen [][]rune, data []byte, srcW, srcH, destW, destH int) {
	// Calcular cuántos bytes por fila hay en los datos de origen
	bytesPerRow := (srcW + 7) / 8 // Ceil(srcW / 8)

	// Recorrer la cuadrícula de la pantalla de destino
	for dy := 0; dy < destH; dy++ {
		for dx := 0; dx < destW; dx++ {
			// Calcular el rango de píxeles de origen que corresponden a esta celda de destino
			// Usamos float64 para mayor precisión en el escalado
			srcXStart := int(float64(dx) * float64(srcW) / float64(destW))
			srcYStart := int(float64(dy) * float64(srcH) / float64(destH))
			srcXEnd := int(float64(dx+1) * float64(srcW) / float64(destW))
			srcYEnd := int(float64(dy+1) * float64(srcH) / float64(destH))

			// Asegurarse de no exceder los límites de la imagen de origen
			if srcXEnd > srcW {
				srcXEnd = srcW
			}
			if srcYEnd > srcH {
				srcYEnd = srcH
			}

			// Verificar si hay algún píxel "encendido" ('1' en los datos) en el rango de origen
			isPixelOn := false
			for sy := srcYStart; sy < srcYEnd; sy++ {
				for sx := srcXStart; sx < srcXEnd; sx++ {
					// Calcular el índice de byte y bit en los datos de origen
					byteIndex := sy*bytesPerRow + sx/8
					bitPos := sx % 8

					// Leer el byte y comprobar el bit correspondiente
					// Asegurarse de que byteIndex está dentro de los límites de 'data'
					if byteIndex < len(data) {
						b := data[byteIndex]
						// Los bits están empaquetados MSB primero (bit 7 es el primer pixel)
						if (b>>(7-bitPos))&1 == 1 {
							isPixelOn = true
							break // Encontramos un píxel encendido, no necesitamos revisar más en este bloque
						}
					}
				}
				if isPixelOn {
					break // Ya encontramos un píxel encendido en esta fila del bloque, salir del bucle de filas
				}
			}

			// Dibujar el carácter en la pantalla de destino
			if isPixelOn {
				screen[dy][dx] = '#' // Usar '#' para píxeles encendidos
			} else {
				screen[dy][dx] = ' ' // Usar espacio para píxeles apagados
			}
		}
	}
}


